    data = res.splice(0, res.length - 1)
    grandTotals = res[res.length - 1]

    let mTotal = []

    for(let key in grandTotals){
      if(key == "LGA" || key == "TOTAL"){
        continue
      }
      mTotal.push({
        name: key,
        percentage: `${(grandTotals[key] / grandTotals["TOTAL"] * 100).toFixed(2)}%`,
        votes: grandTotals[key]
      })
    }

    lgaDataTotal["Total_Result"] = mTotal
    console.log("Total recomputed totals: ", lgaDataTotal)

    const recomputedList = data.map((lga) => {
      const candidates = [];
      Object.keys(lga).forEach((d) => {
        if (d == "LGA" || d == "TOTAL") {
          return;
        }
        candidates.push({
          name: d,
          percentage: `${((lga[d] / lga["TOTAL"]) * 100).toFixed(2)}%`,
          votes: lga[d],
        });
      });

      return {
        title: "Top 3 candidates",
        lgaName: lga["LGA"],
        candidates: candidates,
      };
    });

    recomputedList.forEach((lga) => {
      lgaData[lga.lgaName] = lga;
    });

    // update charts and maps
    const chartOptions = updateChartOptions("all");
    if(chart == null){
      chart = new ApexCharts(ResultChart, chartOptions);
      chart.render()
    }else{
      chart.updateOptions(chartOptions);
    }
    updateMapColors()
