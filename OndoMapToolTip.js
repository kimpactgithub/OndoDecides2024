  function getSeriesDataForLGA(lgaId) {
    // Check if "all" is passed to return the total data from lgaDataTotal
    if (lgaId === "all") {
      return lgaDataTotal.Total_Result
        .sort((a, b) =>
          parseInt(b.votes.replace(",", "")) - parseInt(a.votes.replace(",", ""))
        )
        .map((party) => ({
          x: party.name,
          y: parseInt(party.votes.replace(",", "")),
          fillColor: getPartyColor(party.name),
          strokeColor: "#C23829",
        }));
    }

    // Get individual LGA data if a specific LGA ID is provided
    const lga = lgaData[lgaId];
    if (!lga) return [];

    // Sort candidates by votes in descending order and map to required structure
    return lga.candidates
      .sort(
        (a, b) =>
          parseInt(b.votes.replace(",", "")) - parseInt(a.votes.replace(",", ""))
      )
      .map((candidate) => ({
        x: candidate.name,
        y: parseInt(candidate.votes.replace(",", "")),
        fillColor: getPartyColor(candidate.name),
        strokeColor: "#C23829",
      }));
  }

  function getPartyColor(party) {
    const colors = {
      PDP: "#EC2637",
      APC: "#08C5E0",
      SDP: "#F69336",
      LP: "#57A40A",
      ZLP: "#CDBA2B",
      ADP: "#074178",
      ADC: "#11AE82",
      AAC: "Brown",

      // Add colors for other parties as needed
    };
    return colors[party] || "#B0B0B0"; // Default color if not specified
  }

  function updateMapColors() {
    Object.keys(lgaData).forEach((lgaId) => {
      const lga = lgaData[lgaId];
  
      // Skip if no data or no candidates available for the LGA
      if (!lga || !lga.candidates || !lga.candidates.length) return;
  
      // Find the leading candidate by sorting candidates by votes in descending order
      const leadingCandidate = lga.candidates
        .map((candidate) => ({
          ...candidate,
          votes: parseInt(candidate.votes.replace(",", ""), 10) || 0, // Parse and validate votes
        }))
        .sort((a, b) => b.votes - a.votes)[0]; // Get the candidate with the highest votes
  
      // Get the corresponding color for the leading candidate's party
      const leadingColor = getPartyColor(leadingCandidate.name);
      console.log("leading party color", leadingCandidate, leadingColor)
      // Select the SVG path for the LGA and set its fill color
      const lgaPath = document.getElementById(lgaId);
      if (lgaPath) {
        lgaPath.setAttribute("fill", leadingColor || "#D9D9D9"); // Default color if no match found
      }
    });
  }
  
  updateMapColors();

