  function showTooltip(event) {
    const lgaId = event.target.id;
    const data = lgaData[lgaId];

    // Check if data exists for the specific LGA
    if (data) {
      // Sort candidates by votes in descending order and get the top three
      console.log("attempting to sort ", data.candidates)
      const topCandidates = data.candidates
        .sort(
          (a, b) =>
            parseInt(b.votes.replace(",", "")) -
            parseInt(a.votes.replace(",", ""))
        )
        .slice(0, 3);
      
        console.log("top candidates", topCandidates)

      // Set tooltip content with the top three candidates
      tooltip.innerHTML = `
        <div class="headerToolTip">
          <div class="headerLeftSide">
            <p class="toolTipTitle">${data.title}</p>
          </div>
          <p class="LGA_Name">${data.lgaName}</p>
        </div>
        <ul class="topThreeList">
          ${topCandidates
            .map(
              (candidate, index) => `
            <li id="candidate${index + 1}" class="candidateAnalysis">
              <p>${candidate.name}</p>
             
              <p>${candidate.votes}</p>
            </li>
            ${
              index < topCandidates.length - 1 ? '<hr class="dividerLine">' : ""
            }
          `
            )
            .join("")}
        </ul>
      `;
    }

    // Position tooltip near the mouse cursor
    tooltip.style.display = "flex";
    tooltip.style.left = `${event.pageX + 10}px`;
    tooltip.style.top = `${event.pageY + 10}px`;
  }

